# 첫번째 수를 입력받는다. 
a = 5 
# 두번째 수를 입력받는다. 
b = 3 
# 첫번째 수와 두번째 수의 덧셈 결과를 출력한다. 
print(a+b) 
# 첫번째 수와 두번째 수의 뺄셈 결과를 출력한다. 
print(a-b) 
# 첫번째 수와 두번째 수의 곱셈 결과를 출력한다. 
print(a*b) 
# 첫번째 수와 두번째 수의 나눗셈 결과를 출력한다. 
print(a/b) 
# 첫번째 수와 두번째 수의 몫을 출력한다. 
print(a//b)
# 첫번째 수와 두번째 수의 나머지를 출력한다. 
print(a%b) 
# 첫번째 수와 두번째 수의 제곱값을 출력한다. 
print(a**b) 